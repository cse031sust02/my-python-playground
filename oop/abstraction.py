# What is Abstraction? :
# ========================

# Abstraction means hiding the complexity and only showing the essential features of the object.
# In this way, the user knows what he is doing but not how the work is being done.
# Any classes in our current software project, that hide internal details provide an abstraction.

# Real World example :
# =====================
#
# Abstraction is a general concept which you can find in the real world as well as in OOP languages.
# We drive cars with out caring or knowing what is going underneath. The abstraction allows us 
# to drive a car without knowing the complexity (ie., how the engine transmission, 
# and braking systems work).

